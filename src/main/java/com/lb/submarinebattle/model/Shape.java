public enum Shape {
    SINGLE_CELL,        // Represents single-cell submarine shape
    TWO_CELL,           // Represents two-cell straight submarine shape
    THREE_CELL_STRAIGHT, // Represents three-cell straight shape
    THREE_CELL_CURVED,   // Represents three-cell curved shape
    FOUR_CELL_STRAIGHT,  // Represents four-cell straight shape
    FOUR_CELL_L_SHAPED,  // Represents four-cell L-shaped
    FOUR_CELL_SQUARE,    // Represents four-cell square shape
    FOUR_CELL_PYRAMID,   // Represents four-cell pyramid shape
    FOUR_CELL_Z_SHAPED   // Represents four-cell Z-shaped
}
